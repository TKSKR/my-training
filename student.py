# Инициализация базы данных студентов
students = {}

# Функция для вывода меню
def print_menu():
    print("\nМеню:")
    print("1. Добавить студента и его оценки")
    print("2. Удалить студента по имени")
    print("3. Найти средний балл всех студентов")
    print("4. Найти студента по имени и вывести его оценки")
    print("5. Вывести всех студентов с уникальными наборами оценок")
    print("6. Выйти")

# Функция для добавления студента
def add_student():
    name = input("Введите имя студента: ")
    scores_input = input("Введите оценки студента через пробел: ")
    try:
        scores = list(map(int, scores_input.split()))
        students[name] = scores
        print(f"Студент {name} с оценками {scores} добавлен.")
    except ValueError:
        print("Ошибка: Оценки должны быть числами.")